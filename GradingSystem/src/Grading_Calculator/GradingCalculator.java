    private void calculateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calculateBtnActionPerformed
        double[] num = new double[8];
        double calTot = 0;

        num[0] = Double.parseDouble(ictTxtField.getText());
        num[1] = Double.parseDouble(mathTxtField.getText());
        num[2] = Double.parseDouble(physicsTxtField.getText());
        num[3] = Double.parseDouble(bioTxtField.getText());
        num[4] = Double.parseDouble(chemTxtField.getText());
        num[5] = Double.parseDouble(englishTxtField.getText());
        num[6] = Double.parseDouble(commerceTxtField.getText());
        num[7] = Double.parseDouble(literatureTxtField.getText());

        // loop through fields and calculate the total
        for (int i = 0; i < num.length; i++) {
            calTot = calTot + num[i];
        }

        // calclulate average
        double calAverage = calTot / num.length;

        String total = String.format("%.0f", calTot);
        totalTxtBx.setText(total);

        String average = String.format("%.0f", calAverage);
        avgTxtBx.setText(average);

        // ranking
        if (Integer.parseInt(total) >= 700) {
            rankTxtBx.setText("1st");
        } else if (Integer.parseInt(total) >= 500) {
            rankTxtBx.setText("2nd upper");
        } else if (Integer.parseInt(total) >= 400) {
            rankTxtBx.setText("2nd lower");
        } else if (Integer.parseInt(total) >= 250) {
            rankTxtBx.setText("3rd");
        } else {
            rankTxtBx.setText("Fail");
        }

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.addRow(new Object[]{
            studentIdTxtBx.getText(),
            courseIdBx.getSelectedItem(),
            rankTxtBx.getText(),
            avgTxtBx.getText(),
            totalTxtBx.getText(),
            ictTxtField.getText(),
            mathTxtField.getText(),
            physicsTxtField.getText(),
            bioTxtField.getText(),
            chemTxtField.getText(),
            englishTxtField.getText(),
            commerceTxtField.getText(),
            literatureTxtField.getText(),});

    }//GEN-LAST:event_calculateBtnActionPerformed

